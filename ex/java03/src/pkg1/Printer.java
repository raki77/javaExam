package pkg1;

public class Printer {
	
	public void println(int prm) {
		System.out.println(prm);
	}
	public void println(boolean prm) {
		System.out.println(prm);
	}
	public void println(double prm) {
		System.out.println(prm);
	}
	public void println(String prm) {
		System.out.println(prm);
	}
}
